/////////////////////////////////////////////////////////////////////////////
//	StartProcess:		This function will try to start the requested		/
//							process.										/
//																			/
//	Parameters:			This function will use the info in the GPConInfo	/
//						structure.											/
//		none:																/
//																			/
//	Returns:																/
//		SUCCESS:		  0  = No errors were encountered.					/
//		M9_ERROR:		(-1) = Couldn't perform one of the required tasks.	/
//																			/
/////////////////////////////////////////////////////////////////////////////

int	StartProcess(void)
{

	if (GPConInfo.OptNumber == PRINTIT) {

		PrintFaultFile();

	}

	else if (GPConInfo.OptNumber == CLEANUP) {

		CleanUp();

	}

	return(SUCCESS);
}